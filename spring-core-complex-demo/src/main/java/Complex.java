import java.util.List;
import java.util.Map;
import java.util.Set;

public class Complex {
    List<Integer> complexList;
    Set<Integer> complexSet;
    Map<Character, Integer> complexMap;
    
    
    public List<Integer> getComplexList() {
        return complexList;
    }
    
    public void setComplexList(List<Integer> complexList) {
        this.complexList = complexList;
    }
    
    public Set<Integer> getComplexSet() {
        return complexSet;
    }
    
    public void setComplexSet(Set<Integer> complexSet) {
        this.complexSet = complexSet;
    }
    
    public Map<Character, Integer> getComplexMap() {
        return complexMap;
    }
    
    public void setComplexMap(Map<Character, Integer> complexMap) {
        this.complexMap = complexMap;
    }
}
